// Extrai logo de um texto de categoria (ex: "Jogaram no Milan" → logo do Milan)
export function getClubLogoFromText(text: string): string | null {
  // Ordena por tamanho do nome (maior primeiro) para evitar match parcial
  const sorted = Object.entries(CLUB_LOGOS).sort((a, b) => b[0].length - a[0].length)
  for (const [clubName, logoPath] of sorted) {
    if (text.toLowerCase().includes(clubName.toLowerCase())) return logoPath
  }
  return null
}
