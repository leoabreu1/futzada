export function getDailyWord(): string {
  const today = new Date().toISOString().split('T')[0]
  if (DAILY_SCHEDULE[today]) return DAILY_SCHEDULE[today]
  // Fallback para datas fora do schedule
  const seed = today.split('-').reduce((acc, n) => acc + parseInt(n), 0)
  return WORD_LIST[seed % WORD_LIST.length]
}

export type LetterState = 'correct' | 'present' | 'absent' | 'empty'

export function evaluateGuess(guess: string, answer: string): LetterState[] {
  const result: LetterState[] = Array(guess.length).fill('absent')
  const answerArr = answer.split('')
  const guessArr = guess.split('')
  const used = Array(answer.length).fill(false)

  // Primeira passagem: corretos
  for (let i = 0; i < guessArr.length; i++) {
    if (guessArr[i] === answerArr[i]) {
      result[i] = 'correct'
      used[i] = true
    }
  }

  // Segunda passagem: presentes
  for (let i = 0; i < guessArr.length; i++) {
    if (result[i] === 'correct') continue
    for (let j = 0; j < answerArr.length; j++) {
      if (!used[j] && guessArr[i] === answerArr[j]) {
        result[i] = 'present'
        used[j] = true
        break
      }
    }
  }

  return result
}
